'''podemos cria um loop infinito veja ou um loop controlavel
dependendo FOR o programa poderá ficar dando um loop infinito, cuidado'''

for i in range(100,5000,200): # de 100 a 5000 com passo de 200
         print(i)

'''Podemos usar o FOR com STRINGS e veremos que uma COLEÇÃO poderá
ter varios ITENS, no ex. abaixo a coleção é a STRING e os ITENS são
as LETRAS desta STRING '''

palavra = 'Tony Filho'
for i in  palavra: #Obs que ele pegou as letras de cada palavra
    print(i)
