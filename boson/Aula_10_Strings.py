'''STRINGS'''
'''São tipo de dados usados para registrar informações textuais e arbitrárias de BYTES'''
'''São, na verdade, cadeias de caracteres'''
'''Em PYTHON, uma string é uma seguência de coleção de objetos em uma posição.'''

minha_string = 'texto em aspas, dupla ou simples'.strip()
numero_de_caracteres = len(minha_string) # com isto conseguimos contar
# quantos caracteres temos dentro da STRING.
print('*-*' * 30)
print('{}'.format(numero_de_caracteres))
