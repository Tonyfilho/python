'''analizaremos o string com o OPERADOR IN que pergunta se existe algo dentro da string'''

frase = 'Curso em Video Python'
# o IN é um OPERADOR que da para se fazer analize e pergunta se dentro da variável existe algo que vc procurar
print('Curso' in frase) # perguntamos se a PALAVRA 'Curso' esta dentro da variável FRASE, olhe o respondido:
#True

print('Android' in frase) # perguntamos se a PALAVRA 'Android' esta dentro da variável FRASE, olhe o respondido:
#False