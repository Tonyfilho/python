''''Vamos ver extrutura de dados mais complexas'''
minha_lista_e = ['Tony','Debora', 'Juan'] # "LIST" A LISTA tem como
# Caracterias são: o USO de CHAVES, e é Sempre ordenado, é Mutável podendo adcionar
# ou subtituir objetos, e é 1 OBJETO por VEZ, ela pode ser vazia
# e, é infinita e pode ser adcionado ou removido DINAMICAMENTE.

minha_tupla_e =  ('Tony','Debora','Juan' ) # "TUPLE" A TUPLA tem
# como  Caracterias são: O uso de "PARENTESES ()",
# a Tupla NÃO é MUTÁVEL, não existe o metado append para adcionar
# objetos na TUPLA, ou extend ou remove , conseguimos MODIFICAR
# mas sem mudar a QUANTIDADE, quando usaremos? Quando tevermos um
# numero limitado de OBJETOS de itens.

meu_dicionario_e = {'nome': 'Tony', 'idade': 45} # "DICT" O DICIONARIO tem
# como  Caracterias são: O uso de "COCHETES {}",. Ele funciona iqual
# a DICIONARIO tipo de portugues, o dicionario tem {}"cochete" e um valor
# dentro Ex. Se vermos a palavra "BONITO" é a palavra é a chave e
# o valor é o seguificado  ou seja "Ref.a pessoa ou objeto belo"
# O DICIONARIO sempre tem que ter uma CHAVE e um VALOR
# e são divididos por ":" mas é 1 só objeto.
# a CHAVE tem se STRING o VALOR pode ser INTEIRO, ou BOOLEANO ETC
# o DICIONARIO descreve e é pararecido do JAINSON do JS
# é conhecido como RASHTAB ou RASHTABLE em outras  liguagens

meu_conjunto_e = {'tony', 'debora'} # "SET" O CONJUNTO tem
# como  Caracterias são: O uso de "COCHETES TB {}", mas é a mistura da LISTA e dicionario,
# ele NÃO TEM CHAVE , somente os valores.
# as diferença principais entre ELE e a LISTA são: Não existe objetos REPETIDOS,É dinamico tb e NÃO É ORDENADO,
# não tem INDICES.

